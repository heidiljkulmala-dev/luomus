/** Infer craft from post tags when craft field is absent (e.g. API responses). */
export function inferCraftFromTags(tags: string[]): CraftId | null {
  const normalized = tags.map((t) => t.toLowerCase());
  const tagToCraft: Record<string, CraftId> = {
    beading: "beading",
    jewelry: "beading",
    knitting: "fiber",
    crochet: "fiber",
    yarn: "fiber",
    pottery: "pottery",
    ceramics: "pottery",
    glaze: "pottery",
    painting: "painting",
    watercolor: "painting",
    drawing: "drawing",
    sketching: "drawing",
    illustration: "drawing",
    ink: "drawing",
    silversmithing: "silversmithing",
    silver: "silversmithing",
    metalwork: "silversmithing",
    soldering: "silversmithing",
    macrame: "macrame",
    weaving: "macrame",
    paper: "paper",
    cardmaking: "paper",
    origami: "paper",
    sewing: "sewing",
    embroidery: "sewing",
    quilting: "sewing",
    wood: "wood",
    carving: "wood",
    candle: "candle",
    "soy-wax": "candle",
    soap: "candle",
  };
  for (const tag of normalized) {
    if (tagToCraft[tag]) return tagToCraft[tag];
  }
  return null;
}
